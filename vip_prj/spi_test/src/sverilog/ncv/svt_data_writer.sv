/**
 * Utility class to write data values out to a file.  This base class writes
 * values to a text file using simple name/value format.  This class can be
 * extended to write out different file formats.
 */
class svt_data_writer;

  /** File handle to write values to. */
  int file_handle;

  /** Singleton handle */
  static local svt_data_writer m_inst = new();

  /** Test mode to validate different file formats */
`ifdef SVT_DATA_WRITER_PTTRN_OVER_OPTS
  static bit is_hier_format = 1;
`else
  static bit is_hier_format = 0;
`endif

  // ---------------------------------------------------------------------------
  /** Constructor */
  extern function new();

  // ---------------------------------------------------------------------------
  /**
   * Obtain a handle to the singleton instance.
   * 
   * @return handle to the svt_data_writer singleton instance
   */
  extern static function svt_data_writer get();

  // ---------------------------------------------------------------------------
  /**
   * Write the supplied bit value to the file.
   * 
   * @param prefix String prepended to the prop_name value
   * @param prop_name String written as the property name
   * @param prop_val Bit value that is written
   * @return Always returns 1
   */
  extern virtual function bit write_value_pair_bit(string prefix, string prop_name, bit prop_val);

  // ---------------------------------------------------------------------------
  /**
   * Write the supplied int value to the file.
   * 
   * @param prefix String prepended to the prop_name value
   * @param prop_name String written as the property name
   * @param prop_val INT value that is written
   * @return Always returns 1
   */
  extern virtual function bit write_value_pair_int(string prefix, string prop_name, int prop_val);

  // ---------------------------------------------------------------------------
  /**
   * Write the supplied bit value to the file.
   * 
   * @param prefix String prepended to the prop_name value
   * @param prop_name String written as the property name
   * @param prop_val String value that is written
   * @return Always returns 1
   */
  extern virtual function bit write_value_pair_string(string prefix, string prop_name, string prop_val);

endclass: svt_data_writer

